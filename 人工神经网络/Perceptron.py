import numpy as np
from sklearn.datasets import load_iris 
from sklearn.linear_model import Perceptron
#数据准备
iris=load_iris() 
X=iris.data[:,(2,3)] # length,width  
y=(iris.target==0).astype(np.int) 

per_clf=Perceptron()
per_clf.fit(X,y)

y_pred=per_clf.predict(X) 

print(y_pred)
print(y)
